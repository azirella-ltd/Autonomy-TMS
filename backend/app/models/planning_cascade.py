class PolicyEnvelope(Base):
    """
    S&OP Policy Parameters (Policy Envelope)

    Contains the tunable parameters that govern downstream agents.
    Can be generated by Autonomy S&OP simulation (FULL mode) or
    provided by customer from their existing S&OP process (INPUT mode).

    Feed-forward contract to Supply + Allocation Agents.
    """
    __tablename__ = "planning_policy_envelope"

    id = Column(Integer, primary_key=True, index=True)
    hash = Column(String(64), unique=True, index=True, nullable=False)

    # Context
    config_id = Column(Integer, ForeignKey("supply_chain_configs.id"), nullable=False, index=True)
    customer_id = Column(Integer, ForeignKey("customers.id"), nullable=False, index=True)

    # Source tracking
    generated_by = Column(Enum(PolicySource, name="policy_source"), nullable=False, default=PolicySource.CUSTOMER_INPUT)

    # Safety Stock Targets (weeks of supply by category)
    # Format: {"frozen": 2.5, "refrigerated": 2.0, "dry": 3.0}
    safety_stock_targets = Column(JSON, nullable=False)

    # OTIF Floors by Segment
    # Format: {"strategic": 0.99, "standard": 0.95, "transactional": 0.90}
    otif_floors = Column(JSON, nullable=False)

    # Allocation Reserves (% reserved for priority segments)
    # Format: {"strategic": 0.30, "standard": 0.25}
    allocation_reserves = Column(JSON, nullable=False)

    # Expedite Budget Caps by Category
    # Format: {"frozen": 10000, "refrigerated": 8000, "dry": 5000}
    expedite_caps = Column(JSON, nullable=False)

    # DOS Ceilings (max days of supply)
    # Format: {"frozen": 14, "refrigerated": 10, "dry": 21}
    dos_ceilings = Column(JSON, nullable=True)

    # Supplier Concentration Limits
    # Format: {"Tyson": 0.40, "Hormel": 0.30, ...}
    supplier_concentration_limits = Column(JSON, nullable=True)

    # Financial Guardrails
    total_inventory_cap = Column(Float, nullable=True)  # $ cap
    gmroi_target = Column(Float, nullable=True)

    # Simulation metadata (only in FULL mode)
    simulation_run_id = Column(Integer, nullable=True)
    what_if_scenarios = Column(JSON, nullable=True)
    confidence_intervals = Column(JSON, nullable=True)

    # Period
    effective_date = Column(Date, nullable=False)
    expires_date = Column(Date, nullable=True)

    # Audit
    created_at = Column(DateTime, default=datetime.utcnow)
    created_by = Column(Integer, ForeignKey("users.id"), nullable=True)
    approved_at = Column(DateTime, nullable=True)
    approved_by = Column(Integer, ForeignKey("users.id"), nullable=True)

    # Relationships - use lazy="select" to defer class resolution
    config = relationship("SupplyChainConfig", lazy="select")
    customer = relationship("Customer", lazy="select")
    supply_baseline_packs = relationship("SupplyBaselinePack", back_populates="policy_envelope")

    __table_args__ = (
        Index("ix_policy_envelope_config_date", "config_id", "effective_date"),
    )

    def compute_hash(self) -> str:
        """Compute deterministic hash of policy parameters"""
        data = {
            "config_id": self.config_id,
            "safety_stock_targets": self.safety_stock_targets,
            "otif_floors": self.otif_floors,
            "allocation_reserves": self.allocation_reserves,
            "expedite_caps": self.expedite_caps,
            "effective_date": self.effective_date.isoformat() if self.effective_date else None,
        }
        json_str = json.dumps(data, sort_keys=True)
        return hashlib.sha256(json_str.encode()).hexdigest()[:64]


