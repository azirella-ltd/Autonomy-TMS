class LayerLicense(Base):
    """
    Tracks which cascade layers a group has purchased.

    Enables modular selling: customers buy only the layers they need.
    Layers not purchased show INPUT screens for manual parameter entry.

    Package tiers (bottom-up, each includes all below):
    - Foundation: execution only (deterministic engines)
    - AI Execution: execution + supply_agent + allocation_agent (TRM agents)
    - Planning: + mrs (MRS candidate generation)
    - Enterprise: + sop (S&OP simulation, full closed-loop)
    """
    __tablename__ = "layer_license"

    id = Column(Integer, primary_key=True, index=True)
    customer_id = Column(Integer, ForeignKey("customers.id"), nullable=False, index=True)
    layer = Column(Enum(LayerName, name="layer_name"), nullable=False)
    mode = Column(Enum(LayerMode, name="layer_mode"), nullable=False, default=LayerMode.INPUT)

    # License metadata
    activated_at = Column(DateTime, nullable=True)
    expires_at = Column(DateTime, nullable=True)
    package_tier = Column(String(50), nullable=True)  # foundation, ai_execution, planning, enterprise

    # Audit
    created_at = Column(DateTime, default=datetime.utcnow)
    updated_at = Column(DateTime, default=datetime.utcnow, onupdate=datetime.utcnow)
    updated_by = Column(Integer, ForeignKey("users.id"), nullable=True)

    # Relationships
    customer = relationship("Customer", lazy="select")

    __table_args__ = (
        Index("ix_layer_license_customer_layer", "customer_id", "layer", unique=True),
    )
