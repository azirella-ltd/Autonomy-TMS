class FeedBackSignal(Base):
    """
    Feed-Back Signals from Execution

    Outcome signals that re-tune upstream policy parameters.
    Enables the learning loop: outcome → parameter adjustment.
    """
    __tablename__ = "planning_feedback_signal"

    id = Column(Integer, primary_key=True, index=True)

    # Context
    config_id = Column(Integer, ForeignKey("supply_chain_configs.id"), nullable=False, index=True)
    customer_id = Column(Integer, ForeignKey("customers.id"), nullable=False, index=True)

    # Signal type
    signal_type = Column(String(50), nullable=False, index=True)
    # Types: actual_otif, allocation_shortfall, execution_adherence,
    #        expedite_frequency, eo_writeoff, override_outcome

    # Where measured
    measured_at_layer = Column(String(50), nullable=False)  # execution, allocation, supply, sop

    # What it re-tunes
    fed_back_to = Column(String(50), nullable=False)  # supply_agent, sop, allocation_agent

    # Signal value
    metric_name = Column(String(100), nullable=False)
    metric_value = Column(Float, nullable=False)
    threshold = Column(Float, nullable=True)
    deviation = Column(Float, nullable=True)

    # Detailed breakdown
    details = Column(JSON, nullable=True)
    # e.g., {"segment": "strategic", "sku": "FRZ-001", "actual_otif": 0.88, "floor": 0.99}

    # What parameter should be re-tuned
    suggested_retune = Column(JSON, nullable=True)
    # e.g., {"parameter": "safety_stock_targets.frozen", "current": 2.5, "suggested": 3.0}

    # Linked commits
    supply_commit_id = Column(Integer, ForeignKey("supply_commit.id"), nullable=True)
    allocation_commit_id = Column(Integer, ForeignKey("allocation_commit.id"), nullable=True)

    # Status
    acknowledged = Column(Boolean, default=False)
    actioned = Column(Boolean, default=False)
    actioned_at = Column(DateTime, nullable=True)
    actioned_by = Column(Integer, ForeignKey("users.id"), nullable=True)

    # Timestamps
    measured_at = Column(DateTime, default=datetime.utcnow, index=True)

    __table_args__ = (
        Index("ix_feedback_signal_type_time", "signal_type", "measured_at"),
    )


