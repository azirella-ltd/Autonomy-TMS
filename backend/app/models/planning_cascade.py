"""
Planning Cascade Models

Full cascade from S&OP through Supply Commit and Allocation Commit.
Supports both FULL mode (with S&OP/MPS layers) and INPUT mode (agents only).

Based on:
- FG Supply Planning as AI Labor.pdf
- Integrated Supply Planning as AI Labor.pdf
- Powell SDAM Framework

Feed-Forward Contracts:
- Each layer produces versioned, hashable artifacts
- Artifacts become grounding input for downstream layers
- Preserves decision identity and reproducibility

Feed-Back Signals:
- Outcome signals re-tune upstream policy parameters
- Enables continuous improvement loop
"""

from datetime import datetime, date
from typing import Optional, Dict, Any, List
import hashlib
import json
import enum

from sqlalchemy import (
    Column, Integer, String, Float, DateTime, Date, Text, Boolean, JSON,
    ForeignKey, Index, Enum
)
