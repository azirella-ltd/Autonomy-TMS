class AgentDecisionMetrics(Base):
    """
    Metrics for agent decisions (both Supply and Allocation agents)

    Key metrics from the memo:
    - Touchless Rate: % of decisions not reviewed
    - Agent Score: Outcome lift from agent decisions vs baseline
    - User Score: Outcome lift from human overrides vs agent baseline
    - Human Override Rate: Decisions reviewed ÷ total in scope
    - Override Dependency: Repeated intervention on same decision identity
    - Downstream Coherence: SC vs what Allocation receives; AC vs what execution fulfills
    """
    __tablename__ = "agent_decision_metrics"

    id = Column(Integer, primary_key=True, index=True)

    # Context
    config_id = Column(Integer, ForeignKey("supply_chain_configs.id"), nullable=False, index=True)
    customer_id = Column(Integer, ForeignKey("customers.id"), nullable=False, index=True)
    agent_type = Column(String(50), nullable=False, index=True)  # supply_agent, allocation_agent

    # Period
    period_start = Column(Date, nullable=False)
    period_end = Column(Date, nullable=False)

    # Core metrics
    touchless_rate = Column(Float, nullable=True)  # % not reviewed
    agent_score = Column(Float, nullable=True)  # Outcome lift vs baseline
    user_score = Column(Float, nullable=True)  # Override lift vs agent
    human_override_rate = Column(Float, nullable=True)  # reviewed / total
    override_dependency_ratio = Column(Float, nullable=True)  # Repeated interventions
    downstream_coherence = Column(Float, nullable=True)  # SC→AC or AC→execution match

    # Volume metrics
    total_decisions = Column(Integer, nullable=True)
    auto_submitted = Column(Integer, nullable=True)
    reviewed = Column(Integer, nullable=True)
    overridden = Column(Integer, nullable=True)
    rejected = Column(Integer, nullable=True)

    # Integrity and risk
