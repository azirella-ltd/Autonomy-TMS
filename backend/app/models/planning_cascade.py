class SupplyCommit(Base):
    """
    Supply Commit (SC) - Agent-Owned Decision

    The best demand-informed, constraint-respecting set of supply actions
    (POs, transfer orders) that balances inventory investment against service risk
    within the S&OP policy envelope.

    Decision artifact contains:
    - order_qty[item, source, destination, time_bucket]
    - target_inventory_position
    - projected_otif[item, segment, bucket]
    - projected_inventory_cost
    - supply_pegging[demand → supply action]
    """
    __tablename__ = "supply_commit"

    id = Column(Integer, primary_key=True, index=True)
    hash = Column(String(64), unique=True, index=True, nullable=False)

    # Grounding link
    supply_baseline_pack_id = Column(Integer, ForeignKey("supply_baseline_pack.id"), nullable=False, index=True)
    supply_baseline_pack_hash = Column(String(64), nullable=False)

    # Context
    config_id = Column(Integer, ForeignKey("supply_chain_configs.id"), nullable=False, index=True)
    customer_id = Column(Integer, ForeignKey("customers.id"), nullable=False, index=True)

    # Selected candidate info
    selected_method = Column(String(50), nullable=True)  # Which SupBP candidate was selected/blended

    # Decision artifact
    # Format: [
    #   {
    #     "sku": "FRZ-001",
    #     "supplier_id": "Tyson",
    #     "destination_id": "DC-001",
    #     "order_qty": 500,
    #     "order_date": "2026-02-06",
    #     "expected_receipt_date": "2026-02-10",
    #     "confidence": 0.92,
    #     "rationale": "Inventory position below ROP"
    #   },
    #   ...
    # ]
    recommendations = Column(JSON, nullable=False)

    # Projected outcomes
    projected_inventory = Column(JSON, nullable=True)  # {sku: [day1, day2, ...]}
    projected_otif = Column(Float, nullable=True)
    projected_inventory_cost = Column(Float, nullable=True)
    projected_dos = Column(Float, nullable=True)

    # Supply pegging (demand → supply action)
    supply_pegging = Column(JSON, nullable=True)

    # Integrity violations (block submission)
    integrity_violations = Column(JSON, nullable=True)
    # Format: [{"type": "negative_inventory", "sku": "X", "period": 3, "detail": "..."}]

    # Risk flags (mark suggested)
    risk_flags = Column(JSON, nullable=True)
    # Format: [{"type": "service_risk", "sku": "X", "detail": "OTIF 88% vs 95% floor"}]

    # Status and workflow
    status = Column(Enum(CommitStatus, name="commit_status"), default=CommitStatus.PROPOSED, nullable=False, index=True)
    requires_review = Column(Boolean, default=False)
    review_reason = Column(Text, nullable=True)

    # Agent metadata
    agent_confidence = Column(Float, nullable=True)
    agent_reasoning = Column(Text, nullable=True)

    # Human interaction
    reviewed_by = Column(Integer, ForeignKey("users.id"), nullable=True)
    reviewed_at = Column(DateTime, nullable=True)
    override_details = Column(JSON, nullable=True)  # What was changed

    # Approval
    approved_by = Column(Integer, ForeignKey("users.id"), nullable=True)
    approved_at = Column(DateTime, nullable=True)

    # Timestamps
    created_at = Column(DateTime, default=datetime.utcnow)
    submitted_at = Column(DateTime, nullable=True)

    # Relationships
    supply_baseline_pack = relationship("SupplyBaselinePack", back_populates="supply_commits")
    config = relationship("SupplyChainConfig")
    allocation_commits = relationship("AllocationCommit", back_populates="supply_commit")

    __table_args__ = (
        Index("ix_supply_commit_status_created", "status", "created_at"),
        Index("ix_supply_commit_config_date", "config_id", "created_at"),
    )

    def compute_hash(self) -> str:
        """Compute deterministic hash"""
        data = {
            "supbp_hash": self.supply_baseline_pack_hash,
            "recommendations": self.recommendations,
            "created_at": self.created_at.isoformat() if self.created_at else None,
        }
        json_str = json.dumps(data, sort_keys=True)
        return hashlib.sha256(json_str.encode()).hexdigest()[:64]


