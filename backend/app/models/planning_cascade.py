class SupplyBaselinePack(Base):
    """
    Supply Baseline Pack (SupBP)

    Non-authoritative mathematical grounding for Supply Agent.
    Contains candidate supply plans that the agent may accept, reject, or blend.

    In FULL mode: Multiple parameterized candidates from MRS/MRP engine.
    In INPUT mode: Single candidate from customer's existing MRP output.
    """
    __tablename__ = "supply_baseline_pack"

    id = Column(Integer, primary_key=True, index=True)
    hash = Column(String(64), unique=True, index=True, nullable=False)

    # Grounding link
    policy_envelope_id = Column(Integer, ForeignKey("planning_policy_envelope.id"), nullable=False, index=True)
    policy_envelope_hash = Column(String(64), nullable=False)

    # Context
    config_id = Column(Integer, ForeignKey("supply_chain_configs.id"), nullable=False, index=True)
    customer_id = Column(Integer, ForeignKey("customers.id"), nullable=False, index=True)

    # Source tracking
    generated_by = Column(Enum(PolicySource, name="policy_source"), nullable=False)

    # Candidates
    # Format: [
    #   {
    #     "method": "REORDER_POINT_V1",
    #     "orders": [{"sku": "FRZ-001", "supplier": "Tyson", "qty": 500, "date": "2026-02-10"}, ...],
    #     "projected_inventory": {"FRZ-001": [100, 150, 200, ...], ...},
    #     "projected_cost": 125000,
    #     "projected_otif": 0.96,
    #     "projected_dos": 12.5,
    #     "policy_params": {"safety_factor": 1.5, ...}
    #   },
    #   ...
    # ]
    candidates = Column(JSON, nullable=False)

    # Tradeoff frontier (only in FULL mode with multiple candidates)
    # Format: [{"method": "X", "cost": 100000, "otif": 0.94}, ...]
    tradeoff_frontier = Column(JSON, nullable=True)

    # Demand grounding (from UCF)
    demand_grounding_hash = Column(String(64), nullable=True)
    demand_by_sku = Column(JSON, nullable=True)  # Demand signal per SKU

    # Supply-side constraints snapshot
    supplier_capacities = Column(JSON, nullable=True)
    lead_times = Column(JSON, nullable=True)

    # Period
    planning_horizon_days = Column(Integer, default=28)
    generated_at = Column(DateTime, default=datetime.utcnow)

    # Relationships
    policy_envelope = relationship("PolicyEnvelope", back_populates="supply_baseline_packs")
    config = relationship("SupplyChainConfig")
    supply_commits = relationship("SupplyCommit", back_populates="supply_baseline_pack")

    def compute_hash(self) -> str:
        """Compute deterministic hash"""
        data = {
            "policy_envelope_hash": self.policy_envelope_hash,
            "candidates": self.candidates,
            "generated_at": self.generated_at.isoformat() if self.generated_at else None,
        }
        json_str = json.dumps(data, sort_keys=True)
        return hashlib.sha256(json_str.encode()).hexdigest()[:64]


