def _augment_state_with_ek(
    features: np.ndarray,
    config_id: Optional[int],
    tenant_id: Optional[int],
    product_id: Optional[str] = None,
    site_id: Optional[str] = None,
    supplier_id: Optional[str] = None,
) -> np.ndarray:
    """Append experiential knowledge features to TRM state vector.

    Queries ACTIVE EK entities matching the context and appends conditional
    features. Returns original features unchanged if no EK entities match
    or if EK service is unavailable (backward compatible).

    This is a shared pattern — all 11 TRM executors can use this function.
    """
    if not config_id or not tenant_id:
        return features
    try:
        from app.services.experiential_knowledge_service import ExperientialKnowledgeService
        from app.db.session import sync_session_factory
        db = sync_session_factory()
        try:
            svc = ExperientialKnowledgeService(db=db, tenant_id=tenant_id, config_id=config_id)
            ek_features = svc.get_state_augmentation(
                config_id=config_id,
                product_id=product_id,
                site_id=site_id,
                supplier_id=supplier_id,
            )
            if ek_features:
                extra = np.array(list(ek_features.values()), dtype=np.float32)
                return np.concatenate([features, extra])
        finally:
            db.close()
    except Exception:
        pass  # Non-critical — TRM works without EK features
    return features


@dataclass
class ATPRequest:
    """Request for ATP check"""
    order_id: str
    product_id: str
    location_id: str
    requested_qty: float
    priority: int
    requested_date: Optional[str] = None
    demand_source: Optional[str] = None
    customer_id: Optional[str] = None

    # For multi-line orders
    line_id: Optional[str] = None


@dataclass
class ATPResponse:
    """Response from ATP check"""
    order_id: str
    line_id: Optional[str]

    # Availability
    can_fulfill: bool
    available_qty: float
    promised_qty: float
    promised_date: Optional[str] = None

    # Source of fulfillment
    fulfillment_source: str = "allocation"  # "allocation", "safety_stock", "production"
    consumption_breakdown: Dict[int, float] = field(default_factory=dict)

    # Confidence (from TRM)
    confidence: float = 1.0
    reasoning: str = ""

    # Conformal Decision Theory risk bound (CDT wrapper)
    risk_bound: Optional[float] = None  # P(loss > threshold), lower is safer
    risk_assessment: Optional[Dict] = None  # Full CDT diagnostics

    # Context-aware explanation (populated when explainer is available)
    context_explanation: Optional[Dict] = None

    def to_dict(self) -> Dict[str, Any]:
        result = {
            "order_id": self.order_id,
            "line_id": self.line_id,
            "can_fulfill": self.can_fulfill,
            "available_qty": self.available_qty,
            "promised_qty": self.promised_qty,
            "promised_date": self.promised_date,
            "fulfillment_source": self.fulfillment_source,
            "consumption_breakdown": self.consumption_breakdown,
            "confidence": self.confidence,
            "reasoning": self.reasoning,
        }
        if self.risk_bound is not None:
            result["risk_bound"] = self.risk_bound
        if self.risk_assessment is not None:
            result["risk_assessment"] = self.risk_assessment
        return result


@dataclass
class ATPState:
    """
    State representation for TRM ATP decisions.

    This is the narrow state space that the TRM operates on.
    """
    # Order context
    order_priority: int
    requested_qty: float

    # Allocation state (by priority tier)
    allocation_available: Dict[int, float]  # priority -> available qty

    # Inventory context (from tGNN)
    current_inventory: float
    pipeline_inventory: float
    safety_stock_level: float

    # Demand context
    demand_forecast: float
    demand_uncertainty: float

    def to_features(self) -> np.ndarray:
        """Convert to feature vector for TRM"""
        # Fixed-size feature vector
        features = [
            float(self.order_priority),
            self.requested_qty,
            self.current_inventory,
            self.pipeline_inventory,
            self.safety_stock_level,
            self.demand_forecast,
            self.demand_uncertainty,
        ]

        # Add allocation features (assume max 5 priorities)
        for p in range(1, 6):
            features.append(self.allocation_available.get(p, 0.0))

        return np.array(features, dtype=np.float32)


class ATPExecutorTRM:
    """
    TRM-based ATP Executor for Allocated Available-to-Promise.

    Architecture:
    - tGNN provides: allocations, inventory context, demand signals
    - TRM decides: fulfillment feasibility and quantity within allocation rules

    The TRM is trained on historical ATP decisions and their outcomes.
    It learns to make fast, accurate decisions within the allocation framework.
    """

    def __init__(
        self,
        allocation_service: AllocationService,
        trm_model: Optional[Any] = None,
        use_heuristic_fallback: bool = True,
        aatp_engine: Optional[AATPEngine] = None,
        db: Optional[Any] = None,
        config_id: Optional[int] = None,
        signal_bus: Optional[HiveSignalBus] = None,
    ):
        """
        Initialize ATP Executor.

        Args:
            allocation_service: Service managing priority allocations
            trm_model: Trained TRM model for ATP decisions (optional)
            use_heuristic_fallback: Use rules-based fallback if TRM unavailable
            aatp_engine: Deterministic AATP engine (optional, used for heuristic)
            db: Optional SQLAlchemy Session for persisting decisions
            config_id: Optional config_id for DB persistence
            signal_bus: Optional HiveSignalBus for inter-TRM coordination
        """
        self.allocation_service = allocation_service
        self._engine = aatp_engine

        # Conformal Decision Theory wrapper for risk bounds
        self._cdt_wrapper: Optional[Any] = None
        if _CDT_AVAILABLE:
            try:
                self._cdt_wrapper = get_cdt_registry().get_or_create("atp")
            except Exception:
                pass  # CDT is optional enhancement
        self.trm_model = trm_model
        self.use_heuristic_fallback = use_heuristic_fallback
        self.db = db
        self.config_id = config_id
        self.signal_bus = signal_bus

        # Context-aware explainer (set externally by SiteAgent or caller)
        self.ctx_explainer = None

        # Decision history for TRM training
        self._decision_history: List[Dict[str, Any]] = []

        # Performance metrics
        self._metrics = {
            "total_requests": 0,
            "fulfilled_requests": 0,
            "partial_fills": 0,
            "trm_decisions": 0,
            "heuristic_decisions": 0,
        }

    def _enrich_with_context(
        self,
        response: 'ATPResponse',
        state: 'ATPState',
        request: 'ATPRequest',
    ) -> 'ATPResponse':
        """Enrich ATP response with context-aware reasoning if explainer is available."""
        if self.ctx_explainer is None:
            return response

        try:
            fill_pct = response.promised_qty / max(1, request.requested_qty)
            summary = (
                f"{'Full' if fill_pct >= 1.0 else f'Partial ({fill_pct:.0%})'} "
                f"fulfillment for order {request.order_id}"
            )
            ctx = self.ctx_explainer.generate_inline_explanation(
                decision_summary=summary,
                confidence=response.confidence,
                trm_confidence=response.confidence if self.trm_model else None,
                decision_category='supply_plan',
                decision_value=request.requested_qty * 10,  # Approx cost
            )
            response.reasoning = ctx.explanation
            response.context_explanation = ctx.to_dict()
        except Exception as e:
            logger.debug(f"Context enrichment failed: {e}")

        return response

    def check_atp(
        self,
        request: ATPRequest,
        inventory_context: Optional[Dict[str, float]] = None
    ) -> ATPResponse:
        """
        Check ATP for an order request.

        This is the main entry point for ATP decisions.

        Args:
            request: ATP request details
            inventory_context: Current inventory state (from tGNN or cache)

        Returns:
            ATPResponse with fulfillment decision
        """
        self._metrics["total_requests"] += 1

        # Read hive signals before decision
        self._read_signals_before_decision()

        # Build state for TRM
        state = self._build_atp_state(request, inventory_context)

        # Get decision from TRM or heuristic
        if self.trm_model is not None:
            response = self._trm_decision(request, state)
            self._metrics["trm_decisions"] += 1
        elif self.use_heuristic_fallback:
            response = self._heuristic_decision(request, state)
            self._metrics["heuristic_decisions"] += 1
        else:
            response = ATPResponse(
                order_id=request.order_id,
                line_id=request.line_id,
                can_fulfill=False,
                available_qty=0,
                promised_qty=0,
                reasoning="No TRM model available and heuristic disabled",
            )

        # Emit signals after decision
        self._emit_signals_after_decision(request, state, response)

        # Apply Conformal Decision Theory risk bound
        if self._cdt_wrapper is not None and self._cdt_wrapper.is_calibrated:
            try:
                # Cost estimate: unfulfilled fraction as normalized loss
                fill_rate = response.promised_qty / max(1.0, request.requested_qty)
                cost_estimate = 1.0 - fill_rate  # 0 = full fill, 1 = zero fill
                risk = self._cdt_wrapper.compute_risk_bound(
                    cost_estimate, state.to_features()
                )
                response.risk_bound = risk.risk_bound
                response.risk_assessment = risk.to_dict()
            except Exception as e:
                logger.debug(f"CDT risk bound computation skipped: {e}")

        # Enrich with context-aware reasoning
        response = self._enrich_with_context(response, state, request)

        # Record for training
        self._record_decision(request, state, response)

        return response

    def commit_atp(self, request: ATPRequest, response: ATPResponse) -> ConsumptionResult:
        """
        Commit an ATP decision by consuming from allocations.

        Called when order is confirmed after ATP check.

        Args:
            request: Original ATP request
            response: ATP response to commit

        Returns:
            ConsumptionResult from allocation service
        """
        if not response.can_fulfill or response.promised_qty == 0:
            return ConsumptionResult(
                order_id=request.order_id,
                order_priority=request.priority,
                requested_qty=request.requested_qty,
                fulfilled_qty=0,
            )

        result = self.allocation_service.consume_for_order(
            order_id=request.order_id,
            product_id=request.product_id,
            location_id=request.location_id,
            requested_qty=response.promised_qty,
            order_priority=request.priority,
            demand_source=request.demand_source,
        )

        if result.fulfilled_qty > 0:
            self._metrics["fulfilled_requests"] += 1
        if result.fulfilled_qty > 0 and not result.fully_fulfilled:
            self._metrics["partial_fills"] += 1

        return result

    def _build_atp_state(
        self,
        request: ATPRequest,
        inventory_context: Optional[Dict[str, float]] = None
    ) -> ATPState:
        """Build state representation for TRM"""
        # Get allocation status
        alloc_status = self.allocation_service.get_allocation_status(
            request.product_id, request.location_id
        )

        allocation_available = {
            p: status.get("available", 0)
            for p, status in alloc_status.items()
        }

        # Default inventory context
        inv_ctx = inventory_context or {}

        return ATPState(
            order_priority=request.priority,
            requested_qty=request.requested_qty,
            allocation_available=allocation_available,
            current_inventory=inv_ctx.get("current_inventory", 0),
            pipeline_inventory=inv_ctx.get("pipeline_inventory", 0),
            safety_stock_level=inv_ctx.get("safety_stock", 0),
            demand_forecast=inv_ctx.get("demand_forecast", 0),
            demand_uncertainty=inv_ctx.get("demand_uncertainty", 0),
        )

    def _trm_decision(self, request: ATPRequest, state: ATPState) -> ATPResponse:
        """
        Get ATP decision from TRM model.

        TRM outputs:
        - can_fulfill: binary
        - fulfill_qty: continuous
        - confidence: 0-1
        """
        try:
            features = state.to_features()

            # Experiential Knowledge state augmentation (Alicke's Belief State Bₜ)
            # Appends conditional features from ACTIVE EK entities matching current context.
            # Same pattern applies to all 11 TRM executors.
            features = _augment_state_with_ek(
                features, self._config_id, self._tenant_id,
                product_id=getattr(state, 'product_id', None),
                site_id=getattr(state, 'site_id', None),
            )

            # TRM inference — returns dict with action_logits, fulfill_qty, confidence, value
            output = self.trm_model.predict(features.reshape(1, -1))

            action_idx = int(np.argmax(output["action_logits"][0]))
            can_fulfill = action_idx in (0, 1)  # fulfill or partial
            fulfill_qty = max(0, min(float(output["fulfill_qty"][0, 0]) * request.requested_qty,
                                     request.requested_qty))
            confidence = float(np.clip(output["confidence"][0, 0], 0, 1))

            # Determine consumption breakdown using heuristic
            # (TRM decides quantity, rules determine which tiers)
            breakdown = self._calculate_consumption_breakdown(
                state, request.priority, fulfill_qty
            )

            return ATPResponse(
                order_id=request.order_id,
                line_id=request.line_id,
                can_fulfill=can_fulfill and fulfill_qty > 0,
                available_qty=sum(state.allocation_available.values()),
                promised_qty=fulfill_qty if can_fulfill else 0,
                consumption_breakdown=breakdown,
                confidence=confidence,
                reasoning=f"TRM decision with confidence {confidence:.2f}",
            )

        except Exception as e:
            logger.warning(f"TRM inference failed: {e}, falling back to heuristic")
            return self._heuristic_decision(request, state)

    def _heuristic_decision(self, request: ATPRequest, state: ATPState) -> ATPResponse:
        """
        Heuristic ATP decision when TRM is unavailable.

        Delegates to AATPEngine if available, otherwise implements
        the consumption logic directly:
        1. Own tier first
        2. Bottom-up from lowest priority
        3. Cannot consume above own tier
        """
        # Delegate to deterministic engine if available
        if self._engine is not None:
            try:
                from datetime import date
                from app.core.clock import config_today_sync
                _today = (
                    config_today_sync(self.config_id, self.db)
                    if (self.config_id is not None and self.db is not None)
                    else date.today()
                )
                engine_order = EngineOrder(
                    order_id=request.order_id,
                    product_id=request.product_id,
                    location_id=request.location_id,
                    requested_qty=request.requested_qty,
                    requested_date=_today,
                    priority=Priority.from_value(request.priority),
                    customer_id=request.customer_id or "",
                )
                engine_result = self._engine.check_availability(engine_order)
                breakdown = {p.value: q for p, q in engine_result.consumption_detail}
                return ATPResponse(
                    order_id=request.order_id,
                    line_id=request.line_id,
                    can_fulfill=engine_result.can_fulfill_full or engine_result.available_qty > 0,
                    available_qty=engine_result.available_qty,
                    promised_qty=engine_result.available_qty,
                    consumption_breakdown=breakdown,
                    confidence=1.0,
                    reasoning=engine_result.recommendation or "Engine-based AATP decision",
                )
            except Exception as e:
                logger.warning(f"AATPEngine delegation failed: {e}, using inline heuristic")

        # Build consumption sequence
        num_priorities = max(state.allocation_available.keys()) if state.allocation_available else 5
        sequence = self._build_consumption_sequence(request.priority, num_priorities)

        # Calculate available following the sequence
        available_qty = 0.0
        breakdown: Dict[int, float] = {}
        remaining_need = request.requested_qty

        for priority in sequence:
            if remaining_need <= 0:
                break

            tier_available = state.allocation_available.get(priority, 0)
            consume_from_tier = min(tier_available, remaining_need)

            if consume_from_tier > 0:
                breakdown[priority] = consume_from_tier
                available_qty += consume_from_tier
                remaining_need -= consume_from_tier

        can_fulfill = available_qty > 0
        promised_qty = available_qty if can_fulfill else 0

        # Build reasoning
        if can_fulfill:
            if available_qty >= request.requested_qty:
                reasoning = f"Full fulfillment from tiers: {list(breakdown.keys())}"
            else:
                fill_rate = available_qty / request.requested_qty
                reasoning = f"Partial fulfillment ({fill_rate:.0%}) from tiers: {list(breakdown.keys())}"
        else:
            reasoning = "No allocation available in eligible tiers"

        return ATPResponse(
            order_id=request.order_id,
            line_id=request.line_id,
            can_fulfill=can_fulfill,
            available_qty=sum(state.allocation_available.values()),
            promised_qty=promised_qty,
            consumption_breakdown=breakdown,
            confidence=1.0,  # Heuristic is deterministic
            reasoning=reasoning,
        )

    def _build_consumption_sequence(self, order_priority: int, num_priorities: int) -> List[int]:
        """
        Build consumption sequence per AATP rules.

        1. Own tier first
        2. Lowest tier (highest number)
        3. Work upward toward own tier
        4. Cannot go above own tier
        """
        sequence = [order_priority]  # Own tier first

        # Then from bottom up, stopping at own tier
        for p in range(num_priorities, order_priority, -1):
            if p != order_priority:
                sequence.append(p)

        return sequence

    def _calculate_consumption_breakdown(
        self,
        state: ATPState,
        order_priority: int,
        fulfill_qty: float
    ) -> Dict[int, float]:
        """Calculate which tiers will be consumed for a given fulfillment qty"""
        num_priorities = max(state.allocation_available.keys()) if state.allocation_available else 5
        sequence = self._build_consumption_sequence(order_priority, num_priorities)

        breakdown: Dict[int, float] = {}
        remaining = fulfill_qty

        for priority in sequence:
            if remaining <= 0:
                break

            available = state.allocation_available.get(priority, 0)
            consume = min(available, remaining)

            if consume > 0:
                breakdown[priority] = consume
                remaining -= consume

        return breakdown

    def _record_decision(
        self,
        request: ATPRequest,
        state: ATPState,
        response: ATPResponse
    ):
        """Record decision for TRM training feedback"""
        record = {
            "request": {
                "order_id": request.order_id,
                "product_id": request.product_id,
                "location_id": request.location_id,
                "requested_qty": request.requested_qty,
                "priority": request.priority,
            },
            "state_features": state.to_features().tolist(),
            "response": response.to_dict(),
            "timestamp": str(np.datetime64('now')),
        }

        self._decision_history.append(record)

        # Keep bounded history
        if len(self._decision_history) > 10000:
            self._decision_history = self._decision_history[-10000:]

        # Persist to DB if session available
        self._persist_decision(request, state, response)

    def _persist_decision(
        self,
        request: ATPRequest,
        state: ATPState,
        response: ATPResponse
    ):
        """Persist decision to powell_atp_decisions table."""
        if self.db is None or self.config_id is None:
            return
        try:
            from app.models.powell_decisions import PowellATPDecision
            from app.services.powell.decision_reasoning import atp_reasoning, capture_hive_context
            breakdown = {str(k): v for k, v in (response.consumption_breakdown or {}).items()}
            method = "trm" if self.trm_model else "heuristic"
            hive_ctx = capture_hive_context(
                self.signal_bus, "atp_executor",
                cycle_id=getattr(self, "_cycle_id", None),
                cycle_phase=getattr(self, "_cycle_phase", None),
            )
            row = PowellATPDecision(
                config_id=self.config_id,
                order_id=request.order_id,
                product_id=request.product_id,
                location_id=request.location_id,
                requested_qty=request.requested_qty,
                order_priority=request.priority,
                can_fulfill=response.can_fulfill,
                promised_qty=response.promised_qty,
                consumption_breakdown=breakdown,
                state_features=state.to_features().tolist(),
                decision_method=method,
                confidence=response.confidence,
                decision_reasoning=atp_reasoning(
                    product_id=request.product_id,
                    location_id=request.location_id,
                    requested_qty=request.requested_qty,
                    promised_qty=response.promised_qty,
                    can_fulfill=response.can_fulfill,
                    order_priority=request.priority,
                    confidence=response.confidence,
                    decision_method=method,
                    consumption_breakdown=breakdown,
                ),
                **hive_ctx,
            )
            self.db.add(row)

            # Create pegging record: customer demand → on-hand supply
            if response.can_fulfill and response.promised_qty > 0:
                try:
                    import uuid
                    from app.models.pegging import SupplyDemandPegging
                    # Resolve site_id from location_id
                    site_id = None
                    try:
                        from sqlalchemy import text as _t
                        r = self.db.execute(_t(
                            "SELECT id FROM site WHERE config_id = :c AND name = :n LIMIT 1"
                        ), {"c": self.config_id, "n": request.location_id})
                        sr = r.fetchone()
                        if sr:
                            site_id = sr[0]
                    except Exception:
                        pass
                    if site_id:
                        # Resolve tenant_id from config
                        tenant_id = 0
                        try:
                            r = self.db.execute(_t(
                                "SELECT tenant_id FROM supply_chain_configs WHERE id = :c"
                            ), {"c": self.config_id})
                            tr = r.fetchone()
                            if tr:
                                tenant_id = tr[0]
                        except Exception:
                            pass
                        peg = SupplyDemandPegging(
                            tenant_id=tenant_id,
                            config_id=self.config_id,
                            product_id=request.product_id,
                            site_id=site_id,
                            demand_type="customer_order",
                            demand_id=request.order_id,
                            demand_priority=request.priority,
                            demand_quantity=float(request.requested_qty),
                            supply_type="on_hand",
                            supply_id=f"INV-{request.location_id}",
                            supply_site_id=site_id,
                            pegged_quantity=float(response.promised_qty),
                            pegging_status="firm",
                            chain_id=str(uuid.uuid4())[:16],
                            chain_depth=0,
                            created_by="atp_executor",
                        )
                        self.db.add(peg)
                except Exception as e2:
                    logger.debug("Pegging record creation failed: %s", e2)

        except Exception as e:
            logger.warning(f"Failed to persist ATP decision: {e}")

    # ---- Hive signal methods ------------------------------------------------

    def _read_signals_before_decision(self) -> None:
        """Read relevant hive signals before making ATP decision."""
        if self.signal_bus is None:
            return
        try:
            signals = self.signal_bus.read(
                consumer_trm="atp_executor",
                types={
                    HiveSignalType.QUALITY_REJECT,
                    HiveSignalType.QUALITY_HOLD,
                    HiveSignalType.REBALANCE_INBOUND,
                    HiveSignalType.MO_RELEASED,
                    HiveSignalType.SS_INCREASED,
                    HiveSignalType.SS_DECREASED,
                },
            )
            if signals:
                logger.debug(f"ATP read {len(signals)} hive signals before decision")
        except Exception as e:
            logger.debug(f"ATP signal read failed: {e}")

